if ($_SERVER['REQUEST_METHOD'] == 'POST' && isset($_POST['delete_kta_application'])) {
    if ($has_kta_application) {
        $kta_id_to_delete = $kta_application_id; // Get the ID of the latest KTA application

        // Fetch file paths to delete physical files
        $query_get_files = "SELECT logo_path, ad_file_path, art_file_path, sk_file_path, payment_proof_path, generated_kta_file_path, generated_kta_file_path_pb, generated_kta_file_path_pengda FROM kta_applications WHERE id = ?";
        $stmt_get_files = $conn->prepare($query_get_files);
        if ($stmt_get_files) {
            $stmt_get_files->bind_param("i", $kta_id_to_delete);
            $stmt_get_files->execute();
            $result_files = $stmt_get_files->get_result();
            $files_to_delete = $result_files->fetch_assoc();
            $stmt_get_files->close();

            // Start transaction
            $conn->begin_transaction();
            try {
                // Delete from kta_application_history
                $delete_history_query = "DELETE FROM kta_application_history WHERE application_id = ?";
                $stmt_delete_history = $conn->prepare($delete_history_query);
                if ($stmt_delete_history === false) {
                    throw new Exception("Prepare delete history failed: " . $conn->error);
                }
                $stmt_delete_history->bind_param("i", $kta_id_to_delete);
                if (!$stmt_delete_history->execute()) {
                    throw new Exception("Failed to delete KTA history: " . $stmt_delete_history->error);
                }
                $stmt_delete_history->close();

                // Delete from kta_applications
                $delete_kta_query = "DELETE FROM kta_applications WHERE id = ?";
                $stmt_delete_kta = $conn->prepare($delete_kta_query);
                if ($stmt_delete_kta === false) {
                    throw new Exception("Prepare delete KTA failed: " . $conn->error);
                }
                $stmt_delete_kta->bind_param("i", $kta_id_to_delete);
                if (!$stmt_delete_kta->execute()) {
                    throw new Exception("Failed to delete KTA application: " . $stmt_delete_kta->error);
                }
                $stmt_delete_kta->close();

                // Commit transaction
                $conn->commit();

                // Delete physical files
                if ($files_to_delete) {
                    foreach ($files_to_delete as $column => $filePath) {
                        if (!empty($filePath)) {
                            // Construct full physical path
                            $full_physical_path = $uploadBaseDirPhysical . $filePath;
                            if (file_exists($full_physical_path)) {
                                if (!unlink($full_physical_path)) {
                                    error_log("Failed to delete physical file: " . $full_physical_path);
                                }
                            }
                        }
                    }
                }

                $messages['success'][] = "Pengajuan KTA berhasil dihapus. Anda sekarang dapat mengajukan KTA baru dan mengubah profil Anda.";
                $has_kta_application = false; // Reset status KTA
                $kta_application_id = null;
                $kta_application_details = null;
                $kta_status = '';
                $kta_display_status = 'Belum Ada Pengajuan';
                $can_submit_kta = true;
                $can_edit_profile = true; // Profil bisa diubah setelah pengajuan KTA dihapus

                // Redirect to prevent form resubmission and show success message on KTA Application tab
                header("Location: users.php?tab=kta-application&kta_success_message=" . urlencode("Pengajuan KTA berhasil dihapus. Anda sekarang dapat mengajukan KTA baru dan mengubah profil Anda."));
                exit();

            } catch (Exception $e) {
                $conn->rollback(); // Rollback transaction on error
                $messages['error'][] = "Gagal menghapus pengajuan KTA: " . $e->getMessage();
            }
        } else {
            $messages['error'][] = "Gagal mendapatkan detail file KTA untuk penghapusan.";
        }
    } else {
        $messages['info'][] = "Tidak ada pengajuan KTA yang dapat dihapus.";
    }
}


