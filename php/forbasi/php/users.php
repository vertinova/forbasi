// Helper function to handle file uploads securely.
// RETURNS FILENAME ONLY FOR DB (relative path within the subfolder)
//
// @param string $fileInputName The name of the file input field (from $_FILES).
// @param array $allowedTypes An array of allowed file extensions (e.g., ['jpg', 'pdf']).
// @param string $baseUploadDir The server's physical base directory for all uploads (e.g., __DIR__ . '/uploads/').
// @param string $subfolder The subfolder relative to $baseUploadDir (e.g., 'kta_files/').
// @param string $filePrefix A prefix for the uploaded filename (e.g., 'kta_logo_').
// @param bool $required True if the file upload is mandatory, false otherwise.
// @return array An associative array with:
// 'success' (boolean): true if upload was successful or if file is optional and not provided.
// 'message' (string): Error message if 'success' is false.
// 'path' (string|null): Full server path to the uploaded file.
// 'filename' (string|null): Only the unique filename (e.g., 'kta_files/kta_logo_uniqueid_original.jpg').
// 'uploaded_type' (string): For debugging, showing detected file type.
function handleFileUpload($fileInputName, $allowedTypes, $baseUploadDir, $subfolder, $filePrefix, $required = true, $maxFileSize = 2097152) {
    $uploadDir = $baseUploadDir . $subfolder;

    if (!isset($_FILES[$fileInputName]) || $_FILES[$fileInputName]['error'] === UPLOAD_ERR_NO_FILE) {
        if (!$required) {
            return ['success' => true, 'path' => null, 'filename' => null, 'message' => 'File opsional tidak diunggah.'];
        }
        return ['success' => false, 'message' => 'File ' . $fileInputName . ' tidak ditemukan atau kosong.'];
    }

    $file = $_FILES[$fileInputName];

    if ($file['error'] !== UPLOAD_ERR_OK) {
        $errorMessages = [
            UPLOAD_ERR_INI_SIZE => 'File melebihi ukuran maksimal yang diizinkan server',
            UPLOAD_ERR_FORM_SIZE => 'File melebihi ukuran maksimal yang diizinkan',
            UPLOAD_ERR_PARTIAL => 'File hanya terupload sebagian',
            UPLOAD_ERR_NO_TMP_DIR => 'Folder temporary tidak ditemukan',
            UPLOAD_ERR_CANT_WRITE => 'Gagal menulis file ke disk',
            UPLOAD_ERR_EXTENSION => 'Upload dihentikan oleh ekstensi'
        ];
        $errorMsg = isset($errorMessages[$file['error']]) ? $errorMessages[$file['error']] : 'Error upload (kode: ' . $file['error'] . ')';
        return ['success' => false, 'message' => 'Error upload file ' . $fileInputName . ': ' . $errorMsg];
    }

    if ($file['size'] > $maxFileSize) {
        $sizeKB = round($file['size'] / 1024, 1);
        $maxSizeKB = round($maxFileSize / 1024, 1);
        return ['success' => false, 'message' => 'File ' . $fileInputName . ' terlalu besar (' . $sizeKB . 'KB). Maksimal ' . $maxSizeKB . 'KB.'];
    }

    $fileInfo = pathinfo($file['name']);
    $fileExt = strtolower($fileInfo['extension'] ?? '');

    // Get MIME type
    $finfo = finfo_open(FILEINFO_MIME_TYPE);
    $mime = finfo_file($finfo, $file['tmp_name']);
    finfo_close($finfo);

    // Define allowed MIME types for each extension (comprehensive list)
    $allowedMimes = [
        'jpg' => ['image/jpeg', 'image/jpg', 'image/pjpeg', 'image/jp2', 'image/pipeg', 'image/vnd.swiftview-jpeg', 'image/x-citrix-jpeg', 'image/x-citrix-pjpeg'],
        'jpeg' => ['image/jpeg', 'image/jpg', 'image/pjpeg', 'image/jp2', 'image/pipeg', 'image/vnd.swiftview-jpeg', 'image/x-citrix-jpeg', 'image/x-citrix-pjpeg'],
        'png' => ['image/png', 'image/x-png', 'image/x-citrix-png'],
        'gif' => ['image/gif'],
        'pdf' => ['application/pdf', 'application/x-pdf', 'application/x-bzpdf', 'application/x-gzpdf'],
        'doc' => ['application/msword'],
        'docx' => ['application/vnd.openxmlformats-officedocument.wordprocessingml.document'],
    ];

    // Check if extension is allowed
    if (!in_array($fileExt, $allowedTypes)) {
        return [
            'success' => false,
            'message' => 'Tipe file ' . $fileInputName . ' tidak diizinkan. Hanya ' . implode(', ', $allowedTypes) . ' yang diperbolehkan.',
            'debug' => 'Extension: ' . $fileExt
        ];
    }

    // Check if MIME type matches the extension
    $isValidMime = false;
    if (isset($allowedMimes[$fileExt])) {
        $validMimes = $allowedMimes[$fileExt];
        if (in_array($mime, $validMimes)) {
            $isValidMime = true;
        }
    }

    // If MIME validation fails, log it but still accept common image formats
    // This is a workaround for inconsistent MIME detection across different systems
    if (!$isValidMime) {
        // Log the error for debugging
        error_log("MIME validation warning for {$fileInputName}: Extension={$fileExt}, MIME={$mime}, Expected=" . implode(',', $validMimes ?? []));
        
        // For image files, be more lenient - accept if it starts with 'image/'
        if (in_array($fileExt, ['jpg', 'jpeg', 'png', 'gif']) && strpos($mime, 'image/') === 0) {
            error_log("Accepting image file {$fileInputName} with MIME: {$mime}");
            $isValidMime = true;
        }
    }

    if (!$isValidMime) {
        return [
            'success' => false,
            'message' => 'Tipe MIME file ' . $fileInputName . ' tidak valid untuk ekstensi .' . $fileExt . '. Pastikan file tidak corrupt.',
            'debug' => 'Extension: ' . $fileExt . ', MIME detected: ' . $mime . ', Expected MIME: ' . implode(' or ', $allowedMimes[$fileExt] ?? [])
        ];
    }

    $cleanFileName = preg_replace("/[^a-zA-Z0-9\._-]/", "_", $file['name']);
    $fileName = $filePrefix . uniqid() . '_' . $cleanFileName;
    $targetPath = $uploadDir . $fileName;

    if (!is_dir($uploadDir)) {
        if (!mkdir($uploadDir, 0755, true)) {
            return ['success' => false, 'message' => 'Gagal membuat direktori upload: ' . $uploadDir];
        }
    }

    if (!is_writable($uploadDir)) {
        return ['success' => false, 'message' => 'Direktori upload tidak bisa ditulisi: ' . $uploadDir];
    }

    if (move_uploaded_file($file['tmp_name'], $targetPath)) {
        // Return filename relative to the base uploads directory (e.g., 'kta_files/filename.jpg')
        return ['success' => true, 'path' => $targetPath, 'filename' => $subfolder . $fileName];
    } else {
        error_log("Failed to move uploaded file. Temp: " . $file['tmp_name'] . " to: " . $targetPath);
        return ['success' => false, 'message' => 'Gagal menyimpan file ' . $fileInputName . '. Silakan coba lagi.'];
    }
}

