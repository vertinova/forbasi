    /**
     * Submit Competition Re-Registration via AJAX
     * @param {Event} event - Form submit event
     * @param {number} competitionId - Competition ID
     */
    function submitCompetitionReregistration(event, competitionId) {
        event.preventDefault();
        
        // Prevent double submission
        if (isSubmittingForm) {
            console.log('Form already submitting, preventing double submit');
            return false;
        }
        
        // Validate form first
        if (!validateCompetitionForm(competitionId)) {
            return false;
        }
        
        // Set submitting flag
        isSubmittingForm = true;
        
        const form = document.getElementById(`competition_form_${competitionId}`);
        const submitBtn = document.getElementById(`submit_btn_${competitionId}`);
        const formData = new FormData(form);
        
        // Disable button and show loading
        submitBtn.disabled = true;
        submitBtn.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Mengirim...';
        
        // Show progress notification
        displayAlert('info', 'Sedang mengirim dokumen... Mohon tunggu.');
        
        // Submit via AJAX
        fetch('users.php', {
            method: 'POST',
            body: formData
        })
        .then(response => {
            console.log('📥 Response status:', response.status);
            console.log('📥 Response headers:', response.headers.get('content-type'));
            
            // Check if response is JSON
            const contentType = response.headers.get('content-type');
            if (!contentType || !contentType.includes('application/json')) {
                throw new Error('Server returned non-JSON response. Content-Type: ' + contentType);
            }
            
            return response.text().then(text => {
                console.log('📥 Raw response:', text.substring(0, 500)); // Log first 500 chars
                try {
                    return JSON.parse(text);
                } catch (e) {
                    console.error('❌ JSON parse error:', e);
                    console.error('❌ Response text:', text);
                    throw new Error('Invalid JSON response from server');
                }
            });
        })
        .then(data => {
            console.log('📦 Parsed data:', data);
            
            // Reset submitting flag
            isSubmittingForm = false;
            
            if (data.success) {
                // Success! Show modal
                console.log('✅ Registration successful');
                
                // Update button
                submitBtn.innerHTML = '<i class="fas fa-check-circle"></i> Dokumen Telah Terkirim';
                submitBtn.classList.remove('btn-success');
                submitBtn.classList.add('btn-secondary');
                
                // Show success modal
                setTimeout(() => {
                    showRegistrationSuccessModal();
                }, 300);
                
            } else {
                // Error
                console.error('❌ Registration failed:', data.message);
                
                // Re-enable button
                submitBtn.disabled = false;
                submitBtn.innerHTML = '<i class="fas fa-paper-plane"></i> Kirim Dokumen Daftar Ulang Kompetisi';
                
                // Show error message
                displayAlert('danger', data.message || 'Terjadi kesalahan. Silakan coba lagi.');
            }
        })
        .catch(error => {
            // Network error
            console.error('❌ Fetch error:', error);
            isSubmittingForm = false;
            
            // Re-enable button
            submitBtn.disabled = false;
            submitBtn.innerHTML = '<i class="fas fa-paper-plane"></i> Kirim Dokumen Daftar Ulang Kompetisi';
            
            // Show error message
            displayAlert('danger', 'Terjadi kesalahan: ' + error.message);
        });
        
        return false;
    }
    
    // Make function globally accessible
    window.submitCompetitionReregistration = submitCompetitionReregistration;
    
