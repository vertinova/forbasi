// Function to validate file size (500KB limit)
function validateFileSize($file, $fieldName) {
    if (!isset($file) || $file['error'] === UPLOAD_ERR_NO_FILE) {
        return ['valid' => false, 'message' => "File {$fieldName} tidak ditemukan."];
    }
    
    if ($file['error'] !== UPLOAD_ERR_OK) {
        return ['valid' => false, 'message' => "Error upload file {$fieldName}."];
    }
    
    if ($file['size'] > MAX_FILE_SIZE) {
        $sizeKB = round($file['size'] / 1024, 1);
        return ['valid' => false, 'message' => "File {$fieldName} terlalu besar ({$sizeKB}KB). Maksimal 500KB."];
    }
    
    return ['valid' => true, 'message' => 'Valid'];
}

