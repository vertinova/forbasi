    // Validation function for competition re-registration form
    let isSubmittingForm = false; // Prevent double submission
    
    function validateCompetitionForm(competitionId) {
        // Prevent double submission
        if (isSubmittingForm) {
            console.log('Form already submitting, preventing double submit');
            return false;
        }
        
        const requiredFiles = [
            { id: `school_letter_${competitionId}`, name: 'Surat Izin Sekolah' },
            { id: `parent_letter_${competitionId}`, name: 'Surat Izin Orang Tua' },
            { id: `team_photo_${competitionId}`, name: 'Foto Tim Lengkap (20 Orang)', maxSize: 2 * 1024 * 1024 },
            { id: `komandan_photo_${competitionId}`, name: 'Foto Komandan' },
            { id: `manager_photo_${competitionId}`, name: 'Foto Manager' },
            { id: `pelatih_photo_${competitionId}`, name: 'Foto Pelatih' },
            { id: `payment_proof_${competitionId}`, name: 'Bukti Pembayaran', maxSize: 2 * 1024 * 1024 }
        ];
        
        // Optional files (cadangan)
        const optionalFiles = [
            { id: `cadangan_1_photo_${competitionId}`, name: 'Foto Cadangan 1' },
            { id: `cadangan_2_photo_${competitionId}`, name: 'Foto Cadangan 2' }
        ];

        // Add pasukan photos
        for (let i = 1; i <= 15; i++) {
            requiredFiles.push({ 
                id: `pasukan_${i}_photo_${competitionId}`, 
                name: `Foto Pasukan ${i}` 
            });
        }

        const missingFiles = [];
        const oversizedFiles = [];
        
        for (const file of requiredFiles) {
            const input = document.getElementById(file.id);
            if (!input || !input.files || input.files.length === 0) {
                missingFiles.push(file.name);
            } else {
                // Check file size for photos with different limits
                const fileObj = input.files[0];
                const maxSize = file.maxSize || MAX_FILE_SIZE;
                
                if (file.name.includes('Foto') && fileObj.size > maxSize) {
                    const sizeKB = Math.round(fileObj.size / 1024);
                    const maxSizeKB = Math.round(maxSize / 1024);
                    oversizedFiles.push(`${file.name} (${sizeKB} KB, maks ${maxSizeKB} KB)`);
                }
            }
        }

        if (oversizedFiles.length > 0) {
            const message = `<strong>⚠️ File berikut melebihi batas ukuran:</strong><br><br>
                ${oversizedFiles.join('<br>')}<br><br>
                <small>Catatan: Foto individual maks 500 KB, Foto Tim & Dokumen maks 2 MB.</small>`;
            showNotification('error', message);
            return false;
        }

        if (missingFiles.length > 0) {
            const message = `<strong>❌ File berikut wajib diupload:</strong><br><br>${missingFiles.join('<br>')}<br><br><small>Total file yang harus diupload: ${requiredFiles.length}</small>`;
            showNotification('error', message);
            
            // Focus on first missing file
            const firstMissingId = requiredFiles.find(f => missingFiles.includes(f.name))?.id;
            if (firstMissingId) {
                const element = document.getElementById(firstMissingId);
                if (element) {
                    element.closest('.form-group')?.scrollIntoView({ behavior: 'smooth', block: 'center' });
                }
            }
            
            return false;
        }

        // All validation passed - set submitting flag
        isSubmittingForm = true;
        
        // Disable submit button to prevent double click
        const form = document.getElementById(`competition_form_${competitionId}`);
        const submitButton = form.querySelector('button[type="submit"]');
        if (submitButton) {
            submitButton.disabled = true;
            submitButton.innerHTML = '<i class="fas fa-spinner fa-spin"></i> Mengirim...';
        }
        
        // Show loading notification
        showNotification('info', '⏳ Sedang mengirim data... Mohon tunggu, jangan tutup halaman ini.');
        
        console.log('Form validation passed, submitting...');
        
        return true;
    }
    
