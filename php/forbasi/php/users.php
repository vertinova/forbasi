        $club_name = trim($_POST['club_name'] ?? '');
        $phone = trim($_POST['phone'] ?? '');
        $province_id = $_POST['province_id'] ?? null;
        $city_id = $_POST['city_id'] ?? null;
        $address = trim($_POST['address'] ?? '');

        // Basic validation
        if (empty($club_name) || empty($phone) || empty($province_id) || empty($city_id) || empty($address)) {
            $messages['error'][] = "Semua field profil wajib diisi.";
        } else {
            // Validate phone number format (simple check)
            if (!preg_match('/^[0-9\-\+ ]+$/', $phone)) {
                $messages['error'][] = "Format nomor telepon tidak valid.";
            } elseif (strlen($address) > 45) {
                $messages['error'][] = "Alamat club tidak boleh lebih dari 45 karakter.";
            } else {
                // Prepare update query
                $update_profile_query = "UPDATE users SET club_name = ?, phone = ?, province_id = ?, city_id = ?, address = ? WHERE id = ?";
                $stmt_update_profile = $conn->prepare($update_profile_query);

                if ($stmt_update_profile === false) {
                    $messages['error'][] = "Prepare update profile failed: " . $conn->error;
                } else {
                    $stmt_update_profile->bind_param("ssiisi", $club_name, $phone, $province_id, $city_id, $address, $user_id);
                    if ($stmt_update_profile->execute()) {
                        $messages['success'][] = "Profil berhasil diperbarui!";
                        // Refresh user data after update
                        $user['club_name'] = $club_name;
                        $user['phone'] = $phone;
                        $user['province_id'] = $province_id;
                        $user['city_id'] = $city_id;
                        $user['address'] = $address;

                        // Optionally, update province and city names for display
                        $query_location_names = "SELECT p.name AS province_name, c.name AS city_name FROM provinces p JOIN cities c ON p.id = c.province_id WHERE p.id = ? AND c.id = ?";
                        $stmt_location_names = $conn->prepare($query_location_names);
                        if ($stmt_location_names) {
                            $stmt_location_names->bind_param("ii", $province_id, $city_id);
                            $stmt_location_names->execute();
                            $result_location_names = $stmt_location_names->get_result();
                            if ($row_location_names = $result_location_names->fetch_assoc()) {
                                $user['province_name'] = $row_location_names['province_name'];
                                $user['city_name'] = $row_location_names['city_name'];
                            }
                            $stmt_location_names->close();
                        }

                        // Redirect to prevent form resubmission and show success message on profile tab
                        header("Location: users.php?tab=profile&success_message=" . urlencode($messages['success'][0]));
                        exit();

                    } else {
                        $messages['error'][] = "Gagal memperbarui profil: " . $conn->error;
                    }
