// Helper function to render pagination links
function renderPagination($total_records, $records_per_page, $current_page, $param_prefix, $search_param_name, $current_search_value, $additional_filters = []) {
    if ($records_per_page == 0) {
        error_log("renderPagination called with records_per_page = 0. Total records: " . $total_records);
        return '';
    }
    
    $total_pages = ceil($total_records / $records_per_page);
    $output = '';

    $filter_query_string = '';
    foreach ($additional_filters as $key => $value) {
        if (!empty($value)) {
            $filter_query_string .= '&' . urlencode($key) . '=' . urlencode($value);
        }
    }

    if ($total_pages > 1) {
        $output .= '<div class="pagination">';
        if ($current_page > 1) {
            $output .= '<a href="?'.$param_prefix.'_page=' . ($current_page - 1) . '&'.$search_param_name.'=' . urlencode(trim($current_search_value, '%') ?? '') . $filter_query_string . '" class="page-link prev"><i class="fas fa-chevron-left"></i> Sebelumnya</a>';
        }

        $start_page = max(1, $current_page - 2);
        $end_page = min($total_pages, $current_page + 2);

        if ($start_page > 1) {
            $output .= '<a href="?'.$param_prefix.'_page=1&'.$search_param_name.'=' . urlencode(trim($current_search_value, '%') ?? '') . $filter_query_string . '" class="page-link">1</a>';
            if ($start_page > 2) {
                $output .= '<span class="page-dots">...</span>';
            }
        }

        for ($i = $start_page; $i <= $end_page; $i++) {
            $active_class = ($i == $current_page) ? 'active' : '';
            $output .= '<a href="?'.$param_prefix.'_page=' . $i . '&'.$search_param_name.'=' . urlencode(trim($current_search_value, '%') ?? '') . $filter_query_string . '" class="page-link ' . $active_class . '">' . $i . '</a>';
        }

        if ($end_page < $total_pages) {
            if ($end_page < $total_pages - 1) {
                $output .= '<span class="page-dots">...</span>';
            }
            $output .= '<a href="?'.$param_prefix.'_page=' . $total_pages . '&'.$search_param_name.'=' . urlencode(trim($current_search_value, '%') ?? '') . $filter_query_string . '" class="page-link">' . $total_pages . '</a>';
        }

        if ($current_page < $total_pages) {
            $output .= '<a href="?'.$param_prefix.'_page=' . ($current_page + 1) . '&'.$search_param_name.'=' . urlencode(trim($current_search_value, '%') ?? '') . $filter_query_string . '" class="page-link next">Selanjutnya <i class="fas fa-chevron-right"></i></a>';
        }
        $output .= '</div>';
    }
    return $output;
}

