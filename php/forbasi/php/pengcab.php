                        FROM kta_applications ka
                        JOIN users u ON ka.user_id = u.id
                        WHERE ka.province_id = ? AND ka.city_id = ?
                        AND ka.status IN ('pending', 'rejected_pengda', 'rejected_pb') 
                        AND (ka.club_name LIKE ? OR ka.leader_name LIKE ? OR u.email LIKE ? OR u.phone LIKE ?)";
    $stmt_count = $conn->prepare($count_query);
    if ($stmt_count) {
        // Prepare parameters for bind_param
        $bind_params_count = [$admin_province_id, $admin_city_id, $search_term, $search_term, $search_term, $search_term];
        $types_count = "iissss";
        
        // Dynamically bind parameters by reference (for PHP 5.6-7.x compatibility)
