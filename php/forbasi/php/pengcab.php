                $row['payment_proof_path_display'] = $row['payment_proof_path'] ? BASE_URL . $upload_dir_relative . $row['payment_proof_path'] : '';
                
                $row['pengcab_payment_proof_path_display'] = $row['pengcab_payment_proof_path'] ? BASE_URL . $upload_dir_relative . $row['pengcab_payment_proof_path'] : '';

