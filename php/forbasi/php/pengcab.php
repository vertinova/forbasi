                            FROM kta_applications ka
                            JOIN users u ON ka.user_id = u.id
                            LEFT JOIN provinces p ON ka.province_id = p.id
                            LEFT JOIN cities c ON ka.city_id = c.id
                            WHERE ka.province_id = ? AND ka.city_id = ?
                            AND ka.status IN ('pending', 'rejected_pengda', 'rejected_pb')
                            AND (ka.club_name LIKE ? OR ka.leader_name LIKE ? OR u.email LIKE ? OR u.phone LIKE ?)
                            ORDER BY
                                CASE
                                    WHEN ka.status = 'rejected_pengda' OR ka.status = 'rejected_pb' THEN 1 
                                    WHEN ka.status = 'pending' THEN 2
                                    ELSE 3
                                END,
                                ka.created_at DESC
                            LIMIT ? OFFSET ?";

    $stmt_kta = $conn->prepare($query_kta);
    if ($stmt_kta) {
        // Prepare parameters for bind_param
        $bind_params_kta = [$admin_province_id, $admin_city_id, $search_term, $search_term, $search_term, $search_term, $limit, $offset];
        $types_kta = "iissssii";

