function fetchBranchMembers($conn, $admin_province_id, $admin_city_id, $limit, $offset, $search_term, $kta_status_filter) {
    global $upload_dir_relative, $generated_kta_subdir;

    $members = [];
    $total_records = 0;

    $conditions = ["u.role_id = 1", "u.province_id = ?", "u.city_id = ?"];
    $param_types_base = "ii";
    $param_values_base = [$admin_province_id, $admin_city_id];

    $kta_join_clause = "LEFT JOIN kta_applications ka ON u.id = ka.user_id AND ka.id = (
        SELECT ka2.id FROM kta_applications ka2 
        WHERE ka2.user_id = u.id 
        ORDER BY ka2.created_at DESC 
        LIMIT 1
    )";
    $kta_status_select = "CASE
        WHEN ka.id IS NULL THEN 'not_applied'
        WHEN ka.status = 'kta_issued' THEN 'issued'
        ELSE 'not_issued'
    END AS kta_status";
    $group_by_clause = ""; // Remove GROUP BY since we're getting latest KTA per user via subquery

    // Adjust conditions and param_types/values based on kta_status_filter
    if ($kta_status_filter == 'issued') {
        $conditions[] = "ka.status = 'kta_issued'";
    } elseif ($kta_status_filter == 'not_issued') {
        $conditions[] = "ka.status NOT IN ('kta_issued') AND ka.id IS NOT NULL";
    } elseif ($kta_status_filter == 'not_applied') {
        $conditions[] = "ka.id IS NULL";
    }
    
    $search_conditions = "(u.username LIKE ? OR u.email LIKE ? OR u.phone LIKE ?)";
    $conditions[] = $search_conditions;
    
    // Combine all parameters and types
    $param_values = array_merge($param_values_base, [$search_term, $search_term, $search_term]);
    $param_types = $param_types_base . "sss";

    $where_clause = "WHERE " . implode(" AND ", $conditions);

    $count_query = "SELECT COUNT(DISTINCT u.id) AS total
                    FROM users u
                    " . $kta_join_clause . "
                    " . $where_clause;
    
    $stmt_count = $conn->prepare($count_query);
    if ($stmt_count) {
        $bind_params_count_stmt = $param_values; // Use the already prepared values
        $types_count_stmt = $param_types; // Use the already prepared types

        // Dynamically bind parameters by reference
        $refs_count_stmt = [];
        foreach ($bind_params_count_stmt as $key => $value) {
            $refs_count_stmt[$key] = &$bind_params_count_stmt[$key];
        }
        array_unshift($refs_count_stmt, $types_count_stmt);

        call_user_func_array([$stmt_count, 'bind_param'], $refs_count_stmt);
        
        $stmt_count->execute();
        $count_result = $stmt_count->get_result();
        $total_records = $count_result->fetch_assoc()['total'];
        $stmt_count->close();
    } else {
        error_log("Failed to prepare branch members count query: " . $conn->error);
        return ['error' => "Gagal menghitung total anggota cabang: " . $conn->error, 'total_records' => 0, 'data' => []];
    }


    $query_members = "SELECT u.id, u.username, u.email, u.phone, p.name AS province_name, c.name AS city_name,
                            ka.id AS kta_application_id, ka.logo_path, ka.club_name,
                            " . $kta_status_select . "
                    FROM users u
                    LEFT JOIN provinces p ON u.province_id = p.id
                    LEFT JOIN cities c ON u.city_id = c.id
                    " . $kta_join_clause . "
                    " . $where_clause . "
                    " . $group_by_clause . "
                    ORDER BY u.username ASC
                    LIMIT ? OFFSET ?";
    
    $stmt_members = $conn->prepare($query_members);
    if ($stmt_members) {
        $final_param_values = array_merge($param_values, [$limit, $offset]);
        $final_param_types = $param_types . "ii";

        $refs_members = [];
        foreach ($final_param_values as $key => $value) {
            $refs_members[$key] = &$final_param_values[$key];
        }
        array_unshift($refs_members, $final_param_types);

        call_user_func_array([$stmt_members, 'bind_param'], $refs_members);

        $stmt_members->execute();
        $result_members = $stmt_members->get_result();
        while ($row = $result_members->fetch_assoc()) {
            $row['province_name'] = $row['province_name'] ?? 'N/A';
            $row['city_name'] = $row['city_name'] ?? 'N/A';
            
            // Add logo path display
            $row['logo_path_display'] = $row['logo_path'] ? BASE_URL . $upload_dir_relative . $row['logo_path'] : '';
            
            // Debug: Log logo information
            if (!empty($row['logo_path'])) {
                error_log("Member ID " . $row['id'] . " has logo: " . $row['logo_path'] . " | Display: " . $row['logo_path_display']);
            }
            
            $members[] = $row;
        }
        $stmt_members->close();
    } else {
        error_log("Failed to prepare branch members fetch statement: " . $conn->error);
        return ['error' => "Gagal mengambil data anggota cabang: " . $conn->error, 'total_records' => 0, 'data' => []];
    }
    return ['data' => $members, 'total_records' => $total_records];
}

